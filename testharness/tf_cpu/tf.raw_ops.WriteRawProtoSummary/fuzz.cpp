#define MAX_RANK 4
#define MIN_RANK 0
#define MIN_TENSOR_SHAPE_DIMS_TF 1
#define MAX_TENSOR_SHAPE_DIMS_TF 10

namespace tf_fuzzer_utils {
void logError(const std::string& message, const uint8_t* data, size_t size) {
    std::cerr << "Error: " << message << std::endl;
}
}

tensorflow::DataType parseDataType(uint8_t selector) {
    tensorflow::DataType dtype; 
    switch (selector % 3) {  
        case 0:
            dtype = tensorflow::DT_RESOURCE;
            break;
        case 1:
            dtype = tensorflow::DT_INT64;
            break;
        case 2:
            dtype = tensorflow::DT_STRING;
            break;
    }
    return dtype;
}

uint8_t parseRank(uint8_t byte) {
    constexpr uint8_t range = MAX_RANK - MIN_RANK + 1;
    uint8_t rank = byte % range + MIN_RANK;
    return rank;
}

std::vector<int64_t> parseShape(const uint8_t* data, size_t& offset, size_t total_size, uint8_t rank) {
    if (rank == 0) {
        return {};
    }

    std::vector<int64_t> shape;
    shape.reserve(rank);
    const auto sizeof_dim = sizeof(int64_t);

    for (uint8_t i = 0; i < rank; ++i) {
        if (offset + sizeof_dim <= total_size) {
            int64_t dim_val;
            std::memcpy(&dim_val, data + offset, sizeof_dim);
            offset += sizeof_dim;
            
            dim_val = MIN_TENSOR_SHAPE_DIMS_TF +
                    static_cast<int64_t>((static_cast<uint64_t>(std::abs(dim_val)) %
                                        static_cast<uint64_t>(MAX_TENSOR_SHAPE_DIMS_TF - MIN_TENSOR_SHAPE_DIMS_TF + 1)));

            shape.push_back(dim_val);
        } else {
             shape.push_back(1);
        }
    }

    return shape;
}

template <typename T>
void fillTensorWithData(tensorflow::Tensor& tensor, const uint8_t* data,
                        size_t& offset, size_t total_size) {
    auto flat = tensor.flat<T>();
    const size_t num_elements = flat.size();
    const size_t element_size = sizeof(T);

    for (size_t i = 0; i < num_elements; ++i) {
        if (offset + element_size <= total_size) {
            T value;
            std::memcpy(&value, data + offset, element_size);
            offset += element_size;
            flat(i) = value;
        } else {
            flat(i) = T{};
        }
    }
}

void fillStringTensor(tensorflow::Tensor& tensor, const uint8_t* data,
                      size_t& offset, size_t total_size) {
    auto flat = tensor.flat<tensorflow::tstring>();
    const size_t num_elements = flat.size();

    for (size_t i = 0; i < num_elements; ++i) {
        if (offset < total_size) {
            size_t str_len = std::min(static_cast<size_t>(32), total_size - offset);
            if (str_len > 0) {
                std::string str(reinterpret_cast<const char*>(data + offset), str_len);
                flat(i) = tensorflow::tstring(str);
                offset += str_len;
            } else {
                flat(i) = tensorflow::tstring("");
            }
        } else {
            flat(i) = tensorflow::tstring("");
        }
    }
}

void fillTensorWithDataByType(tensorflow::Tensor& tensor,
                              tensorflow::DataType dtype, const uint8_t* data,
                              size_t& offset, size_t total_size) {
    switch (dtype) {
        case tensorflow::DT_INT64:
            fillTensorWithData<int64_t>(tensor, data, offset, total_size);
            break;
        case tensorflow::DT_STRING:
            fillStringTensor(tensor, data, offset, total_size);
            break;
        default:
            break;
    }
}

extern "C" int LLVMFuzzerTestOneInput(const uint8_t* data, size_t size) {
    std::cout << "Start Fuzzing" << std::endl;
    if (size < 10) return 0;
    
    size_t offset = 0;

    tensorflow::Scope root = tensorflow::Scope::NewRootScope().WithDevice("/cpu:0");

    try {
        auto writer_placeholder = tensorflow::ops::Placeholder(root.WithOpName("writer"), tensorflow::DT_RESOURCE);
        
        uint8_t step_rank = parseRank(data[offset++]);
        auto step_shape = parseShape(data, offset, size, step_rank);
        tensorflow::TensorShape step_tensor_shape;
        for (auto dim : step_shape) {
            step_tensor_shape.AddDim(dim);
        }
        tensorflow::Tensor step_tensor(tensorflow::DT_INT64, step_tensor_shape);
        fillTensorWithDataByType(step_tensor, tensorflow::DT_INT64, data, offset, size);
        auto step_input = tensorflow::ops::Const(root.WithOpName("step"), step_tensor);

        uint8_t tensor_rank = parseRank(data[offset++]);
        auto tensor_shape = parseShape(data, offset, size, tensor_rank);
        tensorflow::TensorShape tensor_tensor_shape;
        for (auto dim : tensor_shape) {
            tensor_tensor_shape.AddDim(dim);
        }
        tensorflow::Tensor tensor_tensor(tensorflow::DT_STRING, tensor_tensor_shape);
        fillTensorWithDataByType(tensor_tensor, tensorflow::DT_STRING, data, offset, size);
        auto tensor_input = tensorflow::ops::Const(root.WithOpName("tensor"), tensor_tensor);

        // Use raw_ops directly instead of summary_ops.h
        tensorflow::Output write_op = tensorflow::Operation(
            root.WithOpName("WriteRawProtoSummary")
                .WithDevice("/cpu:0")
                .WithAttr("T", tensorflow::DT_STRING)
                .WithInput(writer_placeholder)
                .WithInput(step_input)
                .WithInput(tensor_input)
        );

        tensorflow::ClientSession session(root);

        tensorflow::Tensor writer_tensor(tensorflow::DT_RESOURCE, tensorflow::TensorShape({}));
        
        std::vector<std::pair<tensorflow::Output, tensorflow::Tensor>> feed_dict = {
            {writer_placeholder, writer_tensor}
        };

        std::vector<tensorflow::Tensor> outputs;
        tensorflow::Status status = session.Run(feed_dict, {}, {write_op}, &outputs);
        if (!status.ok()) {
            return -1;
        }

    } catch (const std::exception& e) {
        tf_fuzzer_utils::logError("CPU Execution error: " + std::string(e.what()), data, size);
        return -1;
    } 

    return 0;
}
